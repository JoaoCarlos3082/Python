# Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

raio_do_circulo = float(input('Tamanho do raio: '))

area_do_raio = 3.14 * (raio_do_circulo**2)

print(f'Para calcular área de um circulo multiplicamos o raio ({raio_do_circulo}) pelo valor de pi (3,14) que é {area_do_raio}')