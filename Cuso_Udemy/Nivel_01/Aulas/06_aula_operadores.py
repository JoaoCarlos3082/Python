# + soma
# - subretação
# * multiplicação
# / divisão
# // divisao inteira
# % resto de divisão
# > ou >= maior ou maior igual
# < ou <= menor ou menor igual

soma = (1 + 3)

print('soma: ', soma)

subetracao = (2 - 3)

print('subetração: ', subetracao)

multiplicacao = (2 * 10)

print('multiplicação: ', multiplicacao)

divisao = (11/2)

print('divisão: ', divisao)

divisao_inteira = (11 // 2)

print('divisão inteira: ', divisao_inteira)

resto_de_divisao = (11 % 2)

print('resto de divisão: ', resto_de_divisao)